public class TipeDataNumber {
    public static void main(String[] args) {

        // byte(1 byte)
        byte iniByte = 100;

        // short (2 byte)
        short iniShort = 1000;

        // int (4 byte)
        int iniInt = 1000000;

        // long (8 byte)
        long iniLong = 10000;

        System.out.println(iniLong);

        // float (4 byte)
        float iniFloat = 10.10F;

        // double (8 byte)
        double iniDouble = 192.992;

        System.out.println(iniDouble);

        // bilangan desimal
        int decimalNumber = 21;
        System.out.println(decimalNumber);
        int hexNumber = 0xAFF;
        System.out.println(hexNumber);
        int binNumber = 0b01010;
        System.out.println(binNumber);

    }
}

